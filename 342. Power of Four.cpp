class Solution {
public:
    bool isPowerOfFour(int n) {
        return (n > 0) && ((n & (n-1)) == 0) 
            && ((n & 0b01010101010101010101010101010101) != 0);
    }
};
/*
Comment:
判斷使否為4的次方數
判斷integer的二進位是否只有一個1且位於奇數bit
利用 n & (n-1) 即是消除最低位的1的操作
如果消除最低位1以後為0即代表只有一個1
再把n & 0b01010101010101010101010101010101 確認是否為奇數bit
*/
